        [AllowAnonymous]
        public ActionResult ExternalSetUp(string returnUrl)
        {
            AuthenticationResult result = OAuthWebSecurity.VerifyAuthentication(Url.Action("ExternalSetUp", new { ReturnUrl = returnUrl }));
            if (result.IsSuccessful)
            {
                var userName = User.Identity.Name;
                _context.SetUserCredentials(result.Provider, result.ExtraData["accesstoken"], result.UserName, userName);
            }

            return RedirectToLocal("", User.Identity.Name);
        }

        //
        // GET: /Account/ExternalLoginCallback

        [AllowAnonymous]
        public ActionResult ExternalLoginCallback(string returnUrl)
        {
            AuthenticationResult result = OAuthWebSecurity.VerifyAuthentication(Url.Action("ExternalLoginCallback", new { ReturnUrl = returnUrl }));
            if (!result.IsSuccessful)
            {
                return RedirectToAction("ExternalLoginFailure");
            }

            var data = result.ExtraData;
            var accessToken = data["accesstoken"];

            var userName = result.UserName;

            var dbUserName = _context.GetUserName(result.Provider, accessToken, result.UserName);

            if (dbUserName.Equals(String.Empty))
            {
                _context.CreateUser(result.Provider, accessToken, result.UserName, GetDefaultImage());
                FormsAuthentication.SetAuthCookie(userName, false);
            }
            else
            {
                FormsAuthentication.SetAuthCookie(dbUserName, false);
            }
            return RedirectToLocal(returnUrl, dbUserName);
        }

